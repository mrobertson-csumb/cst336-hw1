        <section>
            A Graph Database (obviously) uses graph structures to allow for semantic queries. Graph databases are a form
            of NoSQL databasing that was created to be useful beyond the limitations of relational approaches. However,
            Graph Databases can be a natural fit for storing application data for a wide variety of software.
            A Graph has nodes, edges and properties to represent the information stored within it.
        </section>
