        <section>
            The data items that a graph database works with are collections of nodes and edges. The edges link
            properties or relationships between nodes. The nodes represent entities within the database.
            The graph model explicitly arranges the dependencies between data-nodes. Relational databases and other
            NoSQL approaches link data using implicit connections. By design, graph databases allow simple and quick
            querying of complex data structures. Graph databases can be visualized clearly because of their network-like
            arrangement.
        </section>
